    public String visaAgentNamn (String namn)
    {
        String namnet = "";
        
        try {
        
        String fraga1 = "Select namn from agent where namn = '" + namn + "'";
        String svar1 = idb.fetchSingle(fraga1);
        namnet = svar1;
        }
        
        catch (InfException ex) {
            System.out.println("Ett fel har uppstått " + ex);
        }
        
        return namnet;
    }
    
