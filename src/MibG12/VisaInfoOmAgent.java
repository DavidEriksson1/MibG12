    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (anvandareArAdmin == true)
        {
            ValjAgentFromAdmin valjAgentFromAdmin = new ValjAgentFromAdmin(idb, nuvarandeAgent);
            valjAgentFromAdmin.setVisible(true);
            dispose();
        }
        
        else if (anvandareArAdmin == false)
        {
            HuvudMenyUtomjording hMU = new HuvudMenyUtomjording(idb, nuvarandeUtomjording);
            hMU.setNuvarandeUtomjording(nuvarandeUtomjording);
            hMU.setVisible(true);
            hMU.setValkommenUtomjording();
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

