    private void btnLamnaTillbakaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLamnaTillbakaActionPerformed
        
 /**
 * Metod som tar bort utrustning från innehar_utrustningstabellen.
 * 
 */
        
        String utrAttLamna = jListUtr.getSelectedValue();
        String fragaUtrID = "Select utrustnings_id from utrustning where benamning ='" + utrAttLamna + "'";
        
        try {
            String svarUtrID = idb.fetchSingle(fragaUtrID);
            String taBort = "Delete from innehar_utrustning where utrustnings_id =" + svarUtrID;

            boolean utrValt = jListUtr.isSelectionEmpty();
            
            if (utrValt == false) 
            {
                        idb.fetchSingle(taBort);
                        JOptionPane.showMessageDialog(null, utrAttLamna + " har lämnats tillbaka!");
                        
                        fyllLista();
                    }
                

             else {
                JOptionPane.showMessageDialog(null, "Ingen utrustning vald, vänligen välj en utrustning från listan att lämna tillbaka!");
            }
        }
         catch (InfException ex) {
            JOptionPane.showMessageDialog(null, "Databasfel");
        }
            
    }//GEN-LAST:event_btnLamnaTillbakaActionPerformed

 /**
 * Metod som fyller jListen med de vapen som nuvarandeagent just nu har utkvitterade.
 * 
 */    
    
    public void fyllLista ()
    {
        DefaultListModel listModel = new DefaultListModel();
        jListUtr.setModel(listModel);
        try {
        String fragaAgentID = "Select agent_id from agent where namn = '" + nuvarandeAgent + "'";
        String svarAgentID = idb.fetchSingle(fragaAgentID);
        String allaUtr = "Select benamning from utrustning where utrustnings_id in (Select utrustnings_id from innehar_utrustning where agent_id =" + svarAgentID +")";
        
        ArrayList<String> utrustning = idb.fetchColumn(allaUtr); 
            
            for (String utr : utrustning)
            {
                listModel.addElement(utr);
            }
        
        }
        catch (InfException ex) {
            JOptionPane.showMessageDialog(null, "Databasfel");
        }
    }
    
    
